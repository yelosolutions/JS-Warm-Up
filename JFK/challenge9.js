/*
Create two functions, add and multiply. Each should take two arguments. 
The add function should sum its arguments and return the result, and multiply should multiply its arguments.
Using only these two functions, solve this simple mathematical problem:
36325 * 9824 + 777
*/

var add = (num1, num2) => {
    return num1 + num2;
}

var multiply = (num1, num2) => {
    return num1 * num2;
}

console.log(add(777, multiply(36325, 9824)));